#include <iostream>
#include <string>

std::string getColorName(int colorChoice)
{
    std::string color;
    switch (colorChoice)
    {
    case 1:
        color = "Red";
        break;
    case 2:
        color = "Green";
        break;
    case 3:
        color = "Blue";
        break;
    case 4:
        color = "Yellow";
        break;
    case 5:
        color = "Magenta";
        break;
    case 6:
        color = "Cyan";
        break;
    case 7:
        color = "White";
        break;
    case 8:
        color = "Black";
        break;
    case 9:
        color = "Orange";
        break;
    default:
        std::cout << "Invalid color choice. Defaulting to Black." << std::endl;
        color = "Black";
        break;
    }
    return color;
}
